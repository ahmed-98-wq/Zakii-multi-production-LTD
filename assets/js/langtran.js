const languageselector = document.querySelector('select')
languageselector.addEventListener('change', (event) => {
    setlanguage(event.target.value);
    localStorage.setItem('lang', event.target.value);
});

document.addEventListener('DOMContentLoaded', () => {
    setlanguage(localStorage.getItem("lang"));
});
const setlanguage = (language) => {
    const elements = document.querySelectorAll('[data-il8n]');
    elements.forEach((element) => {
        const translationsKey = element.getAttribute('data-il8n');
        element.textContent = translations[language][translationsKey];
    });
    document.dir = language === "ar" ? "rtl" : "ltr";

    // تغيير الصورة بناءً على اللغة
    const image = document.querySelector('#logoeng'); // تأكد من أن الصورة تحتوي على ID معين
    if (image) {
        if (language === 'ar') {
            image.src = './img/zakii-logo.png'; // مسار الصورة الخاصة باللغة العربية
        } else {
            image.src = './img/zakii-logo2.png'; // مسار الصورة الخاصة باللغة الإنجليزية أو غيرها
        }
    }
    const image1 = document.querySelector('#gll');
    if (image1) {
        if (language === 'ar') {
            image1.src = './img/zakii-ar-post.png'; // مسار الصورة الخاصة باللغة العربية
        } else {
            image1.src = './img/zakii-en-post.png'; // مسار الصورة الخاصة باللغة الإنجليزية أو غيرها
        }
    }
    const image2 = document.querySelector('#gll2');
    if (image2) {
        if (language === 'ar') {
            image2.src = './img/zakii-ar-show.png'; // مسار الصورة الخاصة باللغة العربية
        } else {
            image2.src = './img/zakii-en-show.png'; // مسار الصورة الخاصة باللغة الإنجليزية أو غيرها
        }
    }
    const redim = document.querySelector('#redimg');
    if (redim) {
        if (language === 'ar') {
            redim.src = './img/zakii-logo.png'; // مسار الصورة الخاصة باللغة العربية
        } else {
            redim.src = './img/zakii-logo2.png'; // مسار الصورة الخاصة باللغة الإنجليزية أو غيرها
        }
    }
}


